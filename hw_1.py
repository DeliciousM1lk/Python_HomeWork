"""
Модуль для работы с генераторами и преобразованиями списков.
Содержит функции для создания словарей, множеств и списков,
а также функцию-генератор для чисел, делящихся на 7.
"""

def create_odd_cubed_dict(lst):
    """
    Возвращает словарь, где ключи — нечётные числа из списка,
    а значения — их кубы.
    """
    return {x: x ** 3 for x in lst if x % 2 != 0}


def create_even_set(lst):
    """
    Возвращает множество чётных чисел из списка.
    """
    return {x for x in lst if x % 2 == 0}


def create_step_list():
    """
    Возвращает список чисел от 0 до 90 включительно с шагом 10.
    """
    return list(range(0, 100, 10))


def divisible_by_seven(n):
    """
    Генератор для чисел, делящихся на 7, в диапазоне от 0 до n включительно.
    """
    for i in range(0, n + 1):
        if i % 7 == 0:
            yield i


if __name__ == "__main__":
    LST1 = [1, 2, 3, 4, 5, 6, 7]
    LST2 = [1, 2, 3, 4, 4, 5, 6, 6, 6, 7, 7]
    N = 50

    # Результаты
    print("Словарь нечётных чисел и их кубов:", create_odd_cubed_dict(LST1))
    print("Множество чётных чисел:", create_even_set(LST2))
    print("Список с шагом 10:", create_step_list())
    print("Числа, делящиеся на 7:", list(divisible_by_seven(N)))
