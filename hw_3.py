def collect_user_data():
    """
    Функция для сбора данных пользователя.
    Возвращает словарь с ключами для резюме.
    """
    user_data = {
        "ФИО": input("Введите ваше ФИО: "),
        "Возраст": input("Введите ваш возраст: "),
        "Контактный телефон": input("Введите ваш контактный телефон: "),
        "Электронная почта": input("Введите ваш email: "),
        "Адрес": input("Введите ваш адрес: "),
        "Желаемая должность": input("Введите желаемую должность: "),
        "Опыт работы": input("Опишите ваш опыт работы: "),
        "Образование": input("Введите ваше образование: "),
        "Навыки": input("Перечислите ваши навыки: "),
        "Дополнительная информация": input("Дополнительная информация: ")
    }
    return user_data

def display_resume(user_data):
    """
    Функция для вывода резюме на основе собранных данных.
    """
    print("\n--- РЕЗЮМЕ ---")
    print(f"ФИО: {user_data['ФИО']}")
    print(f"Возраст: {user_data['Возраст']}")
    print(f"Контактный телефон: {user_data['Контактный телефон']}")
    print(f"Электронная почта: {user_data['Электронная почта']}")
    print(f"Адрес: {user_data['Адрес']}")
    print(f"Желаемая должность: {user_data['Желаемая должность']}")
    print(f"Опыт работы: {user_data['Опыт работы']}")
    print(f"Образование: {user_data['Образование']}")
    print(f"Навыки: {user_data['Навыки']}")
    print(f"Дополнительная информация: {user_data['Дополнительная информация']}")
    print("--- Конец резюме ---\n")

if __name__ == "__main__":
    user_data = collect_user_data()
    display_resume(user_data)
